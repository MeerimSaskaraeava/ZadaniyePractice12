public class Boy {
    private String boyName;
    private String study;
    private byte age;


     void setBoyName(String boyName){
         this.boyName=boyName;
     }
     void setStudy(String study){
         this.study=study;
     }
     void setAge(byte age){
         this.age=age;
     }
     String getBoyName(){
         return boyName;
     }
     String getStudy(){
         return study;
     }
     byte getAge(){
         return age;
     }

}

